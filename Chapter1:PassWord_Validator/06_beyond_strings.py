# >>> # With `and`: to return true, both sides must be true
# >>> True and True
# True
# >>> True and False
# False
# >>> False and True
# False

# >>> # With `or`: to return true, only one side needs to be true
# >>> True or False
# True
# >>> False or True
# True
# >>> True or True
# True
# >>> False or False
# False

# >>> # With `not`: returns the opposite of what's given
# >>> not True
# False
# >>> not False
# True

